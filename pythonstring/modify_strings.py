#Python has a set of built-in method that you can use on strings.

a = "Hello Word"
print(a.upper())

a = "Sonu Rathor"
print(a.lower())

#Remove Whitespace
#The strip() method removes any Whitespace from the beginning or the end

a = "Monu rathor  "
print(a.strip())

#Replace String
#The replace() method replaces a string with another string:

a = "Hello Word"
print(a.replace("H", "J"))
